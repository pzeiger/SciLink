def _render_remote_file_picker(
    label: str,
    key_prefix: str,
    extensions: Optional[set[str]] = None,
    multi: bool = False,
) -> List[str]:
    """Compact remote filesystem browser. Returns selected remote paths."""
    conn = st.session_state.get("hpc_connection")
    if conn is None or not conn.is_connected:
        st.caption("Connect to HPC via the sidebar to browse remote files.")
        return []

    PATH_KEY = f"_rfp_{key_prefix}_path"
    SEL_KEY = f"_rfp_{key_prefix}_selected"

    default_cwd = st.session_state.get("hpc_remote_cwd", "~")
    if default_cwd == "~":
        default_cwd = conn.home_dir()

    if PATH_KEY not in st.session_state:
        st.session_state[PATH_KEY] = default_cwd
    if SEL_KEY not in st.session_state:
        st.session_state[SEL_KEY] = []

    selected: List[str] = st.session_state[SEL_KEY]

    if selected:
        for i, path in enumerate(selected):
            c_name, c_rm = st.columns([5, 1])
            with c_name:
                name = PurePosixPath(path).name
                icon = _REMOTE_ICONS.get(PurePosixPath(path).suffix.lower(), "📄")
                st.caption(f"{icon} `{path}`")
            with c_rm:
                if st.button("✕", key=f"rfp_rm_{key_prefix}_{i}"):
                    selected.pop(i)
                    st.session_state[SEL_KEY] = selected
                    st.rerun()

    c_path, c_up, c_home = st.columns([5, 1, 1])
    with c_path:
        with st.form(key=f"rfp_nav_form_{key_prefix}", clear_on_submit=False):
            typed_path = st.text_input(
                "Path", value=st.session_state[PATH_KEY],
                key=f"_rfp_{key_prefix}_path_input",
                label_visibility="collapsed",
            )
            if st.form_submit_button("Go", use_container_width=True):
                if typed_path != st.session_state[PATH_KEY]:
                    st.session_state[PATH_KEY] = typed_path
                    st.rerun()
    with c_up:
        if st.button("⬆", key=f"rfp_up_{key_prefix}"):
            st.session_state[PATH_KEY] = str(
                PurePosixPath(st.session_state[PATH_KEY]).parent
            )
            st.rerun()
    with c_home:
        if st.button("🏠", key=f"rfp_home_{key_prefix}"):
            st.session_state[PATH_KEY] = conn.home_dir()
            st.rerun()

    cwd = st.session_state[PATH_KEY]
    st.caption(f"📂 `{cwd}`")

    try:
        entries = conn.listdir(cwd)
    except Exception as exc:
        st.error(f"Cannot list `{cwd}`: {exc}")
        return selected

    entries.sort(key=lambda e: (
        not (stat_mod.S_ISDIR(e.st_mode) if e.st_mode else False),
        e.filename.lower(),
    ))

    container = st.container(height=240)
    with container:
        has_visible = False
        for entry in entries:
            name = entry.filename
            if name.startswith("."):
                continue
            is_dir = stat_mod.S_ISDIR(entry.st_mode) if entry.st_mode else False
            full_path = f"{cwd.rstrip('/')}/{name}"

            if is_dir:
                has_visible = True
                if st.button(
                    f"📁 {name}/",
                    key=f"rfp_d_{key_prefix}_{cwd}_{name}",
                    use_container_width=True,
                ):
                    st.session_state[PATH_KEY] = full_path
                    st.rerun()
            else:
                ext = PurePosixPath(name).suffix.lower()
                if extensions and ext not in extensions:
                    continue
                has_visible = True
                icon = _REMOTE_ICONS.get(ext, "📄")
                is_sel = full_path in selected
                size = _fmt_size(entry.st_size) if entry.st_size else ""

                c_f, c_s, c_b = st.columns([4, 1.5, 1])
                with c_f:
                    st.caption(f"**{icon} {name}**" if is_sel else f"{icon} {name}")
                with c_s:
                    st.caption(size)
                with c_b:
                    if is_sel:
                        if st.button("✓", key=f"rfp_u_{key_prefix}_{cwd}_{name}"):
                            selected.remove(full_path)
                            st.session_state[SEL_KEY] = selected
                            st.rerun()
                    else:
                        if st.button("＋", key=f"rfp_s_{key_prefix}_{cwd}_{name}"):
                            if multi:
                                selected.append(full_path)
                            else:
                                selected = [full_path]
                            st.session_state[SEL_KEY] = selected
                            st.rerun()

        if not has_visible:
            st.caption("(empty or no matching files)")

    return selected


def _fmt_size(n: int) -> str:
    for unit in ("B", "KB", "MB", "GB"):
        if abs(n) < 1024:
            return f"{n:.0f} {unit}" if unit == "B" else f"{n:.1f} {unit}"
        n /= 1024
    return f"{n:.1f} TB"


def _q(s: str) -> str:
    return "'" + s.replace("'", "'\\''") + "'"


