def _map_host_to_container(host_path: str, mounts: list[dict]) -> str:
    """Map a host path to a container path using the mount list.
    
    Returns the original path if no mount matches (with a warning in logs).
    """
    for mnt in mounts:
        h = mnt.get("host", "").rstrip("/")
        c = mnt.get("container", "").rstrip("/")
        if h and c and host_path.startswith(h):
            return c + host_path[len(h):]
    # No matching mount — return as-is (will fail at runtime)
    return host_path


def _show_path_mapping(
    work_dir: str,
    data_path: Optional[str],
    ff_paths: list[str],
) -> None:
    """Show the user how host paths map into the container."""
    mounts = st.session_state.get("_hpc_mounts", [])
    valid_mounts = [m for m in mounts if m.get("host") and m.get("container")]

    if not valid_mounts:
        st.warning("No volume mounts configured. The container won't see your files.")
        return

    paths_to_check = [("Working dir", work_dir)]
    if data_path:
        paths_to_check.append(("Data file", data_path))
    for fp in ff_paths:
        paths_to_check.append(("FF file", fp))

    all_ok = True
    for label, path in paths_to_check:
        mapped = _map_host_to_container(path, mounts)
        if mapped == path:
            # No mount matched
            st.error(f"⚠️ **{label}**: `{path}` — not covered by any mount!")
            all_ok = False
        else:
            st.caption(f"✅ **{label}**: `{path}` → `{mapped}`")

    if not all_ok:
        st.warning("Add a volume mount that covers the unmapped paths.")

