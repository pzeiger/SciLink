def _upload_text(conn, content: str, remote_path: str) -> None:
    """Upload a string as a remote text file."""
    import tempfile
    with tempfile.NamedTemporaryFile(mode="w", suffix=".tmp", delete=False) as f:
        f.write(content)
        tmp = f.name
    try:
        conn.upload(tmp, remote_path)
    finally:
        os.unlink(tmp)


def _cleanup_monitoring_state() -> None:
    """Clear monitoring-specific session state."""
    for key in [
        "hpc_mon_known_files",
        "hpc_mon_downloaded_images",
        "hpc_gen_run_script",
        "hpc_gen_slurm_script",
        "hpc_gen_remote_work_dir",
        "hpc_gen_job_name",
    ]:
        st.session_state.pop(key, None)
    for k in list(st.session_state.keys()):
        if k.startswith("_rfp_"):
            del st.session_state[k]
