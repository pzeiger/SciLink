# Log parsing for stage progress
# ══════════════════════════════════════════════════════════════

_STAGE_PATTERNS = [
    (re.compile(r"STAGE_MARKER:\s*STARTING", re.I), "Starting"),
    (re.compile(r"stage\s*\d*\s*:\s*minim", re.I), "Minimization"),
    (re.compile(r"stage\s*\d*\s*:\s*(nvt|npt)?\s*equil", re.I), "Equilibration"),
    (re.compile(r"stage\s*\d*\s*:\s*(nvt|npt)?\s*prod", re.I), "Production"),
    (re.compile(r"running\s+(final\s+)?analysis", re.I), "Analysis"),
    (re.compile(r"SIMULATION\s+COMPLETE", re.I), "Complete"),
    (re.compile(r"STAGE_MARKER:\s*FINISHED.*exit=0", re.I), "Complete"),
    (re.compile(r"STAGE_MARKER:\s*FINISHED", re.I), "Finished (check exit code)"),
]

_STAGE_ORDER = [
    "Starting", "Minimization", "Equilibration",
    "Production", "Analysis", "Complete",
]


def _parse_stage(log_text: str) -> tuple[str, float]:
    """Parse the current stage and progress (0.0–1.0) from log output."""
    current = "Pending"
    for pattern, stage_name in _STAGE_PATTERNS:
        if pattern.search(log_text):
            current = stage_name

    # Calculate progress fraction
    if current in _STAGE_ORDER:
        idx = _STAGE_ORDER.index(current)
        progress = idx / max(len(_STAGE_ORDER) - 1, 1)
    else:
        progress = 0.0

    return current, progress


def _count_errors_warnings(log_text: str) -> tuple[int, int]:
    """Count error and warning lines in log output."""
    errors = len(re.findall(r"^.*(?:ERROR|FAILED|FATAL).*$", log_text, re.M | re.I))
    warnings = len(re.findall(r"^.*(?:WARNING|WARN).*$", log_text, re.M | re.I))
    return errors, warnings


