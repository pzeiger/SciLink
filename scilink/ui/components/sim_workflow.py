def _render_results() -> None:
    st.subheader("📋 Simulation Results")

    job_id = st.session_state.get("hpc_monitored_job_id")
    tracked = st.session_state.get("hpc_tracked_jobs", {})
    conn = st.session_state.get("hpc_connection")

    if not job_id or job_id not in tracked:
        st.warning("No results to display.")
        if st.button("← Back", key="res_back"):
            st.session_state.hpc_workflow_phase = "configure"
            st.rerun()
        return

    job = tracked[job_id]

    # Status summary
    m1, m2, m3 = st.columns(3)
    with m1:
        st.metric("Final Status", f"{job.status.emoji} {job.status.value}")
    with m2:
        st.metric("Runtime", job.time_used or "—")
    with m3:
        ec = job.exit_code if job.exit_code is not None else "—"
        st.metric("Exit Code", ec)

    # Try to load the results JSON
    if conn and conn.is_connected and job.work_dir:
        results_path = f"{job.work_dir}/scilink_results.json"
        try:
            import json
            raw = conn.read_text(results_path)
            results = json.loads(raw)
            with st.expander("Orchestrator results (JSON)", expanded=False):
                st.json(results)
        except Exception:
            pass

    # Show all downloaded images
    downloaded = st.session_state.get("hpc_mon_downloaded_images", {})
    images = {
        k: v for k, v in downloaded.items()
        if PurePosixPath(k).suffix.lower() in {".png", ".jpg", ".jpeg"}
    }
    if images:
        st.markdown("### Generated Plots")
        for path, data in images.items():
            st.image(data, caption=PurePosixPath(path).name)

    # Download all results as zip
    if conn and conn.is_connected and job.work_dir:
        st.markdown("---")
        if st.button(
            "💾 Download all results as ZIP",
            key="res_download_all",
            type="primary",
        ):
            try:
                import io
                import zipfile

                entries = conn.listdir(job.work_dir)
                buf = io.BytesIO()
                with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
                    for entry in entries:
                        if stat_mod.S_ISDIR(entry.st_mode) if entry.st_mode else False:
                            continue
                        full = f"{job.work_dir}/{entry.filename}"
                        try:
                            data = conn.read_bytes(full)
                            zf.writestr(entry.filename, data)
                        except Exception:
                            pass
                st.download_button(
                    "💾 Save ZIP",
                    data=buf.getvalue(),
                    file_name=f"simulation_{job_id}.zip",
                    mime="application/zip",
                    key="res_zip_download",
                )
            except Exception as exc:
                st.error(f"Failed: {exc}")

    # Navigation
    st.markdown("---")
    c1, c2 = st.columns(2)
    with c1:
        if st.button("🔄 New Simulation", key="res_new", use_container_width=True):
            _cleanup_monitoring_state()
            st.session_state.hpc_workflow_phase = "configure"
            st.rerun()
    with c2:
        if st.button("📡 Back to Monitor", key="res_to_mon", use_container_width=True):
            st.session_state.hpc_workflow_phase = "monitoring"
            st.rerun()


